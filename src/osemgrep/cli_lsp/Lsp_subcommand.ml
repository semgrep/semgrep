(* All the business logic after command-line parsing. Return the desired
   exit code. *)
let run (_conf : Lsp_CLI.conf) : Exit_code.t = Exit_code.ok

(*****************************************************************************)
(* Entry point *)
(*****************************************************************************)

let main (argv : string array) : Exit_code.t =
  let conf = Lsp_CLI.parse_argv argv in
  run conf
