  | A.ForInitVar (a1, a2), B.ForInitVar (b1, b2) ->
      m_entity a1 b1 >>= fun () -> m_variable_definition a2 b2
  | A.ForInitExpr a1, B.ForInitExpr b1 -> m_expr a1 b1
  | A.ForInitVar _, _ | A.ForInitExpr _, _ -> fail ()

