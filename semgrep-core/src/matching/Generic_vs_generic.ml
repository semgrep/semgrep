and m_block a b =
  match (a.s, b.s) with
  | A.Block _, B.Block _ -> m_stmt a b
  | A.Block (_, [ a_stmt ], _), _ -> m_stmt a_stmt b
  | _, B.Block (_, [ b_stmt ], _) -> m_stmt a b_stmt
  | _, _ -> m_stmt a b

